function modInv(a, n){
    if(gcd(a, n) !== 1) return -1;
    let backup = n;
    let u = 0, v = 1;
    while(n%a != 0){
        let q = Math.floor(n/a);
        let r = n%a;
        let tmp = v;
        v = u - q*v;
        u = tmp;
        n = a;
        a = r;
    }
    return v > 0 ? v : v + backup;
}
function gcd(a, b){
    if(a == 0) return b;
    return gcd(b%a, a);
}

